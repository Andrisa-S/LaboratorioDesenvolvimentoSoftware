    // Método para salvar dados no arquivo
    private void salvarDadosNoArquivo() {
        try (PrintWriter writer = new PrintWriter(new FileWriter(ARQUIVO_DADOS))) {
            for (Aluno aluno : listaAlunos) {
                writer.println(aluno.toFileString());
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, 
                "Erro ao salvar dados: " + e.getMessage(), 
                "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    // Método para carregar dados do arquivo
    private void carregarDadosDoArquivo() {
        File arquivo = new File(ARQUIVO_DADOS);
        if (!arquivo.exists()) {
            return; // Arquivo não existe, não há dados para carregar
        }
        
        try (BufferedReader reader = new BufferedReader(new FileReader(ARQUIVO_DADOS))) {
            String linha;
            listaAlunos.clear(); // Limpa a lista atual
            
            while ((linha = reader.readLine()) != null) {
                Aluno aluno = Aluno.fromFileString(linha);
                if (aluno != null) {
                    listaAlunos.add(aluno);
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, 
                "Erro ao carregar dados: " + e.getMessage(), 
                "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    // Método para atualizar a tabela com os dados da lista
    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) tblAlunos.getModel();
        model.setRowCount(0); // Limpa a tabela
        
        for (Aluno aluno : listaAlunos) {
            model.addRow(aluno.obterDados());
        }
    }
    
