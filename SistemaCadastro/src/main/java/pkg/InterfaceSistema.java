    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNomeCompleto.getText().trim().isEmpty() ||
            txtMatricula.getText().trim().isEmpty() ||
            txtCpf.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Preencha os campos obrigatórios: Nome, Matrícula e CPF", 
                "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(rdFeminino.isSelected()){
            sexo = "F";
        }
        else if(rdMasculino.isSelected()){
            sexo = "M";
        } else {
            JOptionPane.showMessageDialog(this, 
                "Selecione o sexo", 
                "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Cria o novo aluno
        Aluno novoAluno = new Aluno(
            txtNomeCompleto.getText(), 
            txtDataNascimento.getText(), 
            sexo, 
            txtMatricula.getText(), 
            txtCurso.getText(), 
            txtCpf.getText(), 
            txtEndereco.getText(), 
            (String) cmbEstado.getSelectedItem(), 
            txtTelefoneContato.getText()
        );
        
        // Adiciona à lista e atualiza a tabela
        listaAlunos.add(novoAluno);
        
        DefaultTableModel tabela = (DefaultTableModel) tblAlunos.getModel();
        tabela.addRow(novoAluno.obterDados());
        
        // Salva no arquivo
        salvarDadosNoArquivo();
        
        // Limpa os campos
        limparCampos();
        
        JOptionPane.showMessageDialog(this, 
            "Aluno cadastrado com sucesso!", 
            "Sucesso", JOptionPane.INFORMATION_MESSAGE);
 
    }//GEN-LAST:event_btnSalvarActionPerformed

